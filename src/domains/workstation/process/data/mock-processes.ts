export type CanvasNodeData = Record<string, unknown> & {
  node: RedRiseNode
  run?: NodeRun
}

export function getCanvasNodes(): Node<CanvasNodeData>[] {
  return mockCanvasNodes.map((node) => ({
    id: node.id,
    type: "redriseNode",
    position: node.position,
    data: {
      node,
      run: mockNodeRuns.find((run) => run.nodeId === node.id),
    },
  }))
}

export function getCanvasEdges(): Edge[] {
  return mockNodeConnections.map((connection) => ({
    id: connection.id,
    source: connection.sourceNodeId,
    target: connection.targetNodeId,
    label: connection.connectionType,
    animated: connection.connectionType !== "default",
    type: "smoothstep",
  }))
}
